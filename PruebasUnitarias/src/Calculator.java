
public class Calculator {
	public int suma(int a, int b) {
		return a + b;
	}
	
	public int multiplica(int a, int b) {
		return a * b;
	}
	
	public float getPI() {
		return 3.1415926f;
	}
	
	public String getStrPI() {
		return String.valueOf(getPI());
	}
	
	public int divide(int a, int b) {
		return a/b;
	}
	
	
}//Calculator
